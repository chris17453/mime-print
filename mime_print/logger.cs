using System.Diagnostics;

namespace mime_print
{
    static class logger {
        public static void evenlog_error(string error_message) {
            try {
                using (var eventLog = new EventLog("Application")) {
                    eventLog.Source = "mime_print";
                    eventLog.WriteEntry(error_message, EventLogEntryType.Information, 101, 1);
                }
            }
            catch {
                // ignored
            }
        } // end evenlog_error
    }
